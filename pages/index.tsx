export async function getServerSideProps() {
  const query = '*[_type == "properties"] | order(order asc)';

  const properties = await client.fetch(query);

  if (!properties.length) {
    return {
      props: {
        properties: []
      }
    }
  } else {
    return {
      props: {
        properties
      }
    }
  }
}